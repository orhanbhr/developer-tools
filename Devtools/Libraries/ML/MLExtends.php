    const config = 'EncodingSupport:ml';

    //--------------------------------------------------------------------------------------------------------
    // $appdir
    //--------------------------------------------------------------------------------------------------------
    //
    // @var string: NULL
    //
    //--------------------------------------------------------------------------------------------------------
    protected $appdir;

    //--------------------------------------------------------------------------------------------------------
    // $extension
    //--------------------------------------------------------------------------------------------------------
    //
    // @var string: .ml
    //
    //--------------------------------------------------------------------------------------------------------
    protected $extension = '.ml';

    //--------------------------------------------------------------------------------------------------------
    // $lang
    //--------------------------------------------------------------------------------------------------------
    //
    // @var string:
    //
    //--------------------------------------------------------------------------------------------------------
    protected $lang;

    //--------------------------------------------------------------------------------------------------------
    // Constructor
    //--------------------------------------------------------------------------------------------------------
    //
    // __construct()
    //
    //--------------------------------------------------------------------------------------------------------
    public function __construct()
    {
        parent::__construct();

        $mlDir = 'Languages' . DS . 'ML' . DS;

        $this->appdir = SELECT_PROJECT_DIR . $mlDir;

        if( ! is_dir($this->appdir) )
        {
            mkdir($this->appdir, 0755);
        }

        $this->lang = $this->appdir.getLang().$this->extension;
    }

    //--------------------------------------------------------------------------------------------------------
    // Protected Lang File
    //--------------------------------------------------------------------------------------------------------
    //
    // @param array  $rows
    //
    //--------------------------------------------------------------------------------------------------------
    protected function _langFile($app)
    {
        return $this->appdir.$app.$this->extension;
    }
}
